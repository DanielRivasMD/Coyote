// standard libraries
use anyhow::Result as anyResult;
use std::path::PathBuf;

////////////////////////////////////////////////////////////////////////////////////////////////////

// error handler

////////////////////////////////////////////////////////////////////////////////////////////////////

// crate utilities
use crate::utils::{
  io::read_io,
  sql::set_conn_db,
};

////////////////////////////////////////////////////////////////////////////////////////////////////

pub fn load(input: &PathBuf, lang: String) -> anyResult<()> {
  // open database connection
  let conn = set_conn_db()?;

  // read input
  read_io(input.to_path_buf(), lang, conn)?;

  Ok(())
}


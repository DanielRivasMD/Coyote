////////////////////////////////////////////////////////////////////////////////////////////////////

// standard libraries
use anyhow::Result as anyResult;
use std::path::PathBuf;

////////////////////////////////////////////////////////////////////////////////////////////////////

// crate utilities
use crate::utils::{
  reader::reader,
  sql::*,
};

////////////////////////////////////////////////////////////////////////////////////////////////////

pub fn load(input: &PathBuf) -> anyResult<()> {
  // open database connection
  let conn = establish_db_connection()?;

  // read input
  reader(input.to_path_buf(), conn)?;

  Ok(())
}


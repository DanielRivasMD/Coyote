////////////////////////////////////////////////////////////////////////////////////////////////////

// library wrapper
use coyote::*;

////////////////////////////////////////////////////////////////////////////////////////////////////

// standard libraries
use anyhow::Result as anyResult;
use clap::Parser;

////////////////////////////////////////////////////////////////////////////////////////////////////

// crate utilities
use crate::utils::reader::*;
use crate::utils::help::*;
use crate::utils::sql::*;
use crate::cmds::train::train;
use crate::cmds::load::load;

////////////////////////////////////////////////////////////////////////////////////////////////////

fn main() -> anyResult<()> {
  // collect command line arguments
  let params = Cli::parse();

  match &params.command {
    Commands::Load { input } => {
      load(input)?;
    }

    Commands::Train {  } => {
      train()?;
    }
  }

  Ok(())
}

